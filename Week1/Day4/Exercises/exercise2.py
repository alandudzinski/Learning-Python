# exercise2.py - A program that manipulates a dictionary

student = {"name": "Alan", "age": 20, "major": "CS"}

# Add a "gpa": 3.8
student["gpa"] = 3.8

# Update "age" to 21
student["age"] = 21

# Delete "major"
del student["major"]

# Final dictionary looks like {"name": "Alan", "age": 21, "gpa": 3.8}